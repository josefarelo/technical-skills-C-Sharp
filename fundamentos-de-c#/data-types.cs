// IMPRESIÓN DE VALORES LITERALES


// Literales de caracteres
Console.WriteLine('a');    // char (carácter)
Console.WriteLine("b");    // string (cadena de texto)

// Literales de enteros
Console.WriteLine(123);    // int (integer/entero)

// Literales de punto flotante
Console.WriteLine(0.25F);       // float ~6-9 dígitos
Console.WriteLine(2.625);       // double ~15-17 dígitos
Console.WriteLine(12.39816M);   // decimal 28-29 dígitos

// Literales booleanos
Console.WriteLine(true);    // True
Console.WriteLine(false);   // False